// Looping Structures

//------------for loop------------------------
let a;
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

//------------while------------------------
let j=1;
while (j <= 10) {
  console.log(j);
  j++;
}

//------------do while------------------------

let z = 1;
do {
  console.log("Always execute");
  console.log(z)
} while (z <= 10);
